CKEDITOR.on( 'dialogDefinition', function( ev )
{
  var dialogName = ev.data.name;
  var dialogDefinition = ev.data.definition;

  if ( dialogName == 'link' || dialogName == 'image' )
  {
    dialogDefinition.removeContents( 'Upload' );
  }
});
